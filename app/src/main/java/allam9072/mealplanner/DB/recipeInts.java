package allam9072.mealplanner.DB;

public class recipeInts {
    private String name;
    private int value;

    public recipeInts(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }
}

